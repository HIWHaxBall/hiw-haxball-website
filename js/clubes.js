// Funci√≥n para abrir el modal del club
function openClubModal(clubName) {
    const modal = document.getElementById('clubModal');
    const modalClubName = document.getElementById('modalClubName');
    const modalClubLogo = document.getElementById('modalClubLogo');
    const playersContainer = document.getElementById('playersContainer');
    const trophiesContainer = document.getElementById('trophiesContainer');
    
    // Obtener datos del club
    const club = clubsData[clubName];
    
    // Configurar informaci√≥n b√°sica del club
    modalClubName.textContent = clubName;
    modalClubLogo.src = club.logo;
    modalClubLogo.alt = `${clubName} Logo`;
    
    // Generar lista de jugadores
    playersContainer.innerHTML = '';
    club.players.forEach(player => {
        const playerElement = document.createElement('div');
        playerElement.className = 'player';
        playerElement.innerHTML = `
            <span class="player-name">${player.name}</span>
            <span class="player-position">${player.position}</span>
        `;
        playersContainer.appendChild(playerElement);
    });
    
    // Generar lista de trofeos
    trophiesContainer.innerHTML = '';
    if (club.trophies.length === 0) {
        trophiesContainer.innerHTML = '<p>Este club no ha ganado trofeos a√∫n.</p>';
    } else {
        club.trophies.forEach(trophy => {
            const trophyElement = document.createElement('div');
            trophyElement.className = 'trophy';
            trophyElement.innerHTML = `
                <span class="trophy-name">${trophy.name}</span>
                <span class="trophy-year">${trophy.year}</span>
            `;
            trophiesContainer.appendChild(trophyElement);
        });
    }
    
    // Mostrar modal
    modal.style.display = 'block';
    
    // Deshabilitar scroll del body
    document.body.style.overflow = 'hidden';
}

// Funci√≥n para cerrar el modal
function closeClubModal() {
    const modal = document.getElementById('clubModal');
    modal.style.display = 'none';
    
    // Habilitar scroll del body
    document.body.style.overflow = 'auto';
}

// Cerrar modal al hacer clic fuera del contenido
window.onclick = function(event) {
    const modal = document.getElementById('clubModal');
    if (event.target === modal) {
        closeClubModal();
    }
};

// Cerrar modal con tecla ESC
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closeClubModal();
    }
});

